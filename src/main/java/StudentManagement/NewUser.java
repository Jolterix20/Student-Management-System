    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
        if(firstNameField.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please enter First Name");
        }
        else if(lastNameField.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please enter Last Name");
        }
        else if(usernameTextField.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please enter Username");
        }
        else if(passwordTextField.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please enter Password");
        }
        else if(!confirmPasswordField.getText().equals(passwordTextField.getText())){
            JOptionPane.showMessageDialog(this, "Passwords don't match");
        }
        else if(emailField.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please enter Email");
        }
        else if(uidField.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Please enter UID");
        }
        else {
            try {
                
                String firstName = firstNameField.getText();
                String lastName = lastNameField.getText();
                String username = usernameTextField.getText();
                String password = confirmPasswordField.getText();
                int uid = Integer.parseInt(uidField.getText());
                String email = emailField.getText();
                
                Class.forName("com.mysql.jdbc.Driver");
                conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/studenttest", "root", "test");
                
                pst1 = conn.prepareStatement("select * from student1 where username = ?");
                pst1.setString(1,username);
                
                rs = pst1.executeQuery();
                
                pst1 = conn.prepareStatement("select * from student1 where uid = ?");
                pst1.setInt(1, uid);
                
                rs1 = pst1.executeQuery();
                
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Username not available");
                    usernameTextField.requestFocus();
                }
                else if (rs1.next()){
                    JOptionPane.showMessageDialog(this, "Please check UID");
                    uidField.requestFocus();
                } else {

                    pst = conn.prepareStatement("insert into student1 (uid, firstname, lastname, username, password, email) values (?,?,?,?,?,?)");
                
                    pst.setInt(1, uid);
                    pst.setString(2, firstName);
                    pst.setString(3, lastName);
                    pst.setString(4, username);
                    pst.setString(5, password);
                    pst.setString(6, email);

                    pst.executeUpdate();

                    JOptionPane.showMessageDialog(this, "User Created Succesfully!!");

                    firstNameField.setText("");
                    lastNameField.setText("");
                    usernameTextField.setText("");
                    passwordTextField.setText("");
                    confirmPasswordField.setText("");
                    uidField.setText("");
                    emailField.setText("");
                    
                    StudentLogin sl = new StudentLogin();
                    sl.setVisible(true);
                    this.setVisible(false);
                }
                
                
                
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Please Enter Appropriate Data");
            }
           
        }
        
    }//GEN-LAST:event_registerBtnActionPerformed

